"""

1. Class: In object-oriented programming, a class is a blueprint for creating objects (instances) that encapsulate data
and behavior (methods).

2. Instance: An instance is a specific realization of a class, created at runtime, which contains its own set of data
and methods.

3. Encapsulation: Encapsulation is the bundling of data (attributes) and methods (functions) that operate on the data
into a single unit, called a class. It hides the internal state of an object from the outside world and only exposes the necessary functionalities.

4. Abstraction: Abstraction is the concept of simplifying complex systems by modeling only the relevant
aspects of the system while hiding unnecessary details. In programming, classes and objects provide abstraction
by allowing you to focus on the essential features and ignore the implementation details.

5. Inheritance: Inheritance is a mechanism in object-oriented programming that allows a new class
(subclass or derived class) to inherit properties and behavior (methods) from an existing class
(superclass or base class). It promotes code reuse and allows for the creation of a hierarchy of classes.

6. Multiple Inheritance: Multiple inheritance is a feature in some object-oriented programming languages
that allows a class to inherit properties and behavior from multiple parent classes. This enables a subclass
to have characteristics of more than one parent class.

7. Polymorphism: Polymorphism is the ability of objects of different classes to be treated
as objects of a common superclass. It allows methods to be called on objects of different classes
without knowing the specific class of the object at compile time. Polymorphism can be achieved through
method overriding and method overloading.

8. Method Resolution Order (MRO): Method Resolution Order is the order in which classes are searched for a method
or attribute in inheritance hierarchies. In Python, the MRO defines the sequence in which base classes are traversed
when resolving methods or attributes of a class. It is determined using the C3 linearization algorithm.
"""

